package com.chefswithoutlimits.customerchef.dataVO;

public class ChefOredrItem {

	private String itemId = "";
	private String itemImage = "";
	private String itemName = "";
	private String itemPrice = "";
	private String itemCurrency = "";
	private String itemAvailable = "";
	private String itemQuantity = "";
	
	public String getItemId() {
		return itemId;
	}
	public void setItemId(String itemId) {
		this.itemId = itemId;
	}
	public String getItemImage() {
		return itemImage;
	}
	public void setItemImage(String itemImage) {
		this.itemImage = itemImage;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemPrice() {
		return itemPrice;
	}
	public void setItemPrice(String itemPrice) {
		this.itemPrice = itemPrice;
	}
	public String getItemCurrency() {
		return itemCurrency;
	}
	public void setItemCurrency(String itemCurrency) {
		this.itemCurrency = itemCurrency;
	}
	public String getItemAvailable() {
		return itemAvailable;
	}
	public void setItemAvailable(String itemAvailable) {
		this.itemAvailable = itemAvailable;
	}
	public String getItemQuantity() {
		return itemQuantity;
	}
	public void setItemQuantity(String itemQuantity) {
		this.itemQuantity = itemQuantity;
	}
}
